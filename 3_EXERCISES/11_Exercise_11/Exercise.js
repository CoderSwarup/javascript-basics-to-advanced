//1. Check the speed difference among the following loops: while, for, for of, forEach


console.time("while")
let i = 0;
while(i < 5){
  console.log(i);
  i++
}
console.timeEnd("while")




console.time("for")

for (var j = 0; j < 5; j++) {
  console.log(j)
}


console.timeEnd("for")


console.time("dowhile")
let z = 0
do {
  console.log(z)
  z++
} while (z < 5);
console.timeEnd("dowhile")